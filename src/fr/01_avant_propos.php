# Avants-propos

Ce livre décrit un langage de programmation n'étant pas doté
à l'heure actuelle d'une implémentation complète. Il existe
quelques interprètes mais aucun ne prend en charge l'intégralité
des aspects évoqués dans ce livre pour différentes raisons:
évolution du langage depuis l'implémentation, difficultés lié
à certains aspects purement techniques, manque de temps ou de moyens.
\

L'objectif de ce livre est alors de poser les specifications
a priori définitive d'une première réelle version complète
de DABSIC. Ce n'est pas un livre didactique, il ne comprend
pas d'exercice et est plutôt à destination de ceux étant capable
d'orès et déjà de programmer dans un autre langage.\

Ainsi, a terme, j'espère qu'un interprète complet verra le
jour, ouvrant la voie à un large ensemble d'applications à
la structure nouvelle. - et à un nouveau livre, didactique cette fois.

