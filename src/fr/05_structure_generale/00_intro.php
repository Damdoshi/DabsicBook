# Structure générale des formats de configuration

Ce chapitre décrit plusieurs des différents formats de configuration
eixstants avec pour objectif de les comparer avec le champ Dabsic.

une description de tous les formats sera faite. Ensuite, une configuration
particulière sera écrite dans tous les formats proposés.

Les **champs** Dabsic sont le tissu de l'Arbre Dabsic, structure générale
contenant l'intégralité des éléments.

Ils sont à la fois les variables d'un langage de programmation
traditionnel mais également les attributs de la structure
globale de l'environnement.\

Les champs Dabsic peuvent contenir une valeur, d'autres champs
Dabsic rangé à la manière d'un tableau, d'autres champs Dabsic
organisé d'après leurs noms: Notez que ces possibilités ne sont
pas exclusives. Il est tout à fait possible de disposer d'un
champ Dabsic étant une valeur et un tableau ou une table.

Un champ Dabsic peut contenir une fonction à la place d'une valeur. Un champ
Dabsic peut trouver sa valeur dans le résultat d'une expression.\

