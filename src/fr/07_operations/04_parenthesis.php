## Parenthèses

L'opérateur de changement de priorité, les parenthèses:

| Symboles | Fonction |
| -------- | -------- |
| ( ) | L'expression situé entre parenthèse est évaluée et sont résultat est introduit dans l'expression parent. |

Les parenthèses sont récursives: il peut y avoir des parenthèses dans des parenthèses, de la même manière qu'en mathématique.

